# Escreva um programa em Python que peça ao usuário
# para digitar dois números e exiba a soma deles.

num_01 = float(input('Por gentileza, digite o primeiro número para realização da soma: '))
num_02 = float(input('Por gentileza, digite o segundo número para realização da soma: '))
